class TestPackageValidationSkipNames:
    def _manifest(self) -> HubPluginManifest:
        return HubPluginManifest.model_validate(
            {
                "schemaVersion": "1.0",
                "id": "demo",
                "type": "tool",
                "name": "demo",
                "description": "demo",
                "version": "1.0.0",
                "category": "integration",
                "source": {"path": "plugins/tools/api/demo"},
            }
        )

    def test_pycache_is_skipped_not_rejected(self, tmp_path):
        package = tmp_path / "demo"
        package.mkdir()
        (package / "_provider.yaml").write_text("name: demo\n", encoding="utf-8")
        cache = package / "__pycache__"
        cache.mkdir()
        (cache / "noise.pyc").write_bytes(b"\x00\x00")

        # Must not raise — historically this rejected the install with
        # "Disallowed path in package: __pycache__".
        validate_package(package, self._manifest())

    def test_skip_names_contains_known_noise(self):
        for name in ("__pycache__", ".git", ".svn", ".DS_Store"):
            assert name in SKIP_NAMES

    def test_path_escape_still_rejected(self, tmp_path):
        package = tmp_path / "demo"
        package.mkdir()
        outside = tmp_path / "outside.txt"
        outside.write_text("hi", encoding="utf-8")
        # Symlink that escapes the package root must still trip the guard.
        (package / "evil").symlink_to(outside)
        with pytest.raises(ValueError, match="escapes"):
            validate_package(package, self._manifest())
