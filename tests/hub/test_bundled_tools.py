class TestBundledToolRoots:
    def test_discovers_api_subdir_plugins(self, isolated_hub):
        _write_bundled_tool(
            isolated_hub["bundled"],
            plugin_id="onesig_v2_5_3_D20250710",
            service_id="onesig_api",
            version="2.5.3 D20250710",
        )
        roots = catalog._bundled_tool_roots()
        assert ("tool", "onesig_v2_5_3_D20250710") in roots

    def test_skips_group_dirs_themselves(self, isolated_hub):
        _write_bundled_tool(
            isolated_hub["bundled"],
            plugin_id="foo",
            service_id="foo_api",
            version="1.0",
        )
        roots = catalog._bundled_tool_roots()
        # The ``api/`` directory is a grouping container, not a plugin.
        assert ("tool", "api") not in roots

    def test_skips_dirs_without_payload(self, isolated_hub):
        empty = isolated_hub["bundled"] / "plugins" / "tools" / "api" / "empty"
        empty.mkdir(parents=True)
        # Underscore-prefixed files are ignored by _has_direct_tool_payload,
        # so a dir holding only a provider.yaml-without-tool should not surface.
        (empty / "_provider.yaml").write_text("name: empty\n", encoding="utf-8")
        roots = catalog._bundled_tool_roots()
        assert ("tool", "empty") not in roots

    def test_returns_empty_when_no_bundled_dir(self, isolated_hub, monkeypatch):
        # Point FLOCKS_HUB_ROOT at an empty directory.
        empty_hub = isolated_hub["bundled"].parent / "empty_hub"
        empty_hub.mkdir()
        monkeypatch.setenv("FLOCKS_HUB_ROOT", str(empty_hub))
        assert catalog._bundled_tool_roots() == {}


