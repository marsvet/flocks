"""Tests for FlocksHub-bundled tool plugin discovery and install.

Covers the multi-version-tool fix wired through:

* :func:`flocks.hub.catalog._bundled_tool_roots` and
  :func:`flocks.hub.catalog._entry_from_bundled_tool` — bundled tools
  surface in ``list_catalog`` as ``available`` until installed.
* :func:`flocks.hub.installer._resolve_install_destination` — tools
  shipped under ``flockshub/plugins/tools/api/<id>/`` install to
  ``<plugins>/tools/api/<id>/`` so :mod:`flocks.config.api_versioning`
  can still find ``_provider.yaml``.
* :func:`flocks.hub.security.validate_package` — generated bytecode
  caches and VCS metadata are silently skipped instead of failing
  install.
"""

from __future__ import annotations

from pathlib import Path

import pytest
import yaml

from flocks.hub import catalog, local
from flocks.hub.installer import _resolve_install_destination, install_plugin, uninstall_plugin
from flocks.hub.models import HubPluginManifest
from flocks.hub.security import SKIP_NAMES, validate_package


