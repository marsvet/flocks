class TestBundledCatalogEntryState:
    def test_bundled_only_lists_as_available(self, isolated_hub):
        _write_bundled_tool(
            isolated_hub["bundled"],
            plugin_id="onesig_v2_5_3_D20250710",
            service_id="onesig_api",
            version="2.5.3 D20250710",
        )
        entries = catalog.list_catalog(plugin_type="tool")
        match = next((e for e in entries if e.id == "onesig_v2_5_3_D20250710"), None)
        assert match is not None, "bundled tool should appear in catalog"
        assert match.state == "available"
        assert match.installedVersion is None
        assert match.source == "bundled"
        assert match.installPath is None

    def test_installed_bundle_lists_as_installed(self, isolated_hub):
        _write_bundled_tool(
            isolated_hub["bundled"],
            plugin_id="onesig_v2_5_3_D20250710",
            service_id="onesig_api",
            version="2.5.3 D20250710",
        )
        # Drop a matching local install + record, mirroring what
        # ``install_plugin`` would produce.
        install_dir = (
            isolated_hub["home"]
            / ".flocks"
            / "plugins"
            / "tools"
            / "api"
            / "onesig_v2_5_3_D20250710"
        )
        install_dir.mkdir(parents=True)
        (install_dir / "_provider.yaml").write_text(
            "name: onesig_v2_5_3_D20250710\nservice_id: onesig_api\nversion: '2.5.3 D20250710'\n",
            encoding="utf-8",
        )
        record = local.make_record(
            plugin_type="tool",
            plugin_id="onesig_v2_5_3_D20250710",
            version="2.5.3 D20250710",
            source="bundled:plugins/tools/api/onesig_v2_5_3_D20250710",
            install_path=install_dir,
            enabled=True,
            scope="global",
        )
        local.save_installed_record(record)

        entries = catalog.list_catalog(plugin_type="tool")
        match = next(e for e in entries if e.id == "onesig_v2_5_3_D20250710")
        assert match.state == "installed"
        assert match.installedVersion == "2.5.3 D20250710"


