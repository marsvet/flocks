class TestResolveInstallDestination:
    def test_tool_from_bundled_api_preserves_subdir(self, isolated_hub):
        src = _write_bundled_tool(
            isolated_hub["bundled"],
            plugin_id="onesig_v2_5_3_D20250710",
            service_id="onesig_api",
            version="2.5.3 D20250710",
        )
        dst = _resolve_install_destination("tool", "onesig_v2_5_3_D20250710", src, "global")
        assert dst.parent.name == "api", (
            "tool installs from flockshub api/ must keep the api/ subdir "
            "so api_versioning's _provider.yaml scan still finds them"
        )
        assert dst.name == "onesig_v2_5_3_D20250710"

    def test_tool_from_bundled_python_preserves_subdir(self, isolated_hub):
        src = _write_bundled_tool(
            isolated_hub["bundled"],
            plugin_id="some_py_tool",
            service_id="py_api",
            version="1.0",
            group="python",
        )
        dst = _resolve_install_destination("tool", "some_py_tool", src, "global")
        assert dst.parent.name == "python"

    def test_non_tool_uses_default_layout(self, isolated_hub):
        # Skill installs go to ``<plugins>/skills/<id>/`` regardless of
        # source layout — no group-prefix preservation.
        src = isolated_hub["bundled"] / "plugins" / "skills" / "demo-skill"
        src.mkdir(parents=True)
        dst = _resolve_install_destination("skill", "demo-skill", src, "global")
        assert dst.parent.name == "skills"
        assert dst.name == "demo-skill"

    def test_tool_from_external_path_uses_default_layout(self, isolated_hub, tmp_path):
        # Source is OUTSIDE flockshub → can't infer a group → fall back
        # to the default ``<plugins>/tools/<id>/`` shape.
        external = tmp_path / "external" / "weird-tool"
        external.mkdir(parents=True)
        dst = _resolve_install_destination("tool", "weird-tool", external, "global")
        assert dst.name == "weird-tool"
        # Default layout: directly under ``tools/`` (no group dir).
        assert dst.parent.name == "tools"


