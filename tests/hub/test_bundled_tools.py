class TestBundledInstallFlow:
    async def test_install_then_uninstall_roundtrip(self, isolated_hub):
        _write_bundled_tool(
            isolated_hub["bundled"],
            plugin_id="onesig_v2_5_3_D20250710",
            service_id="onesig_api",
            version="2.5.3 D20250710",
        )

        record = await install_plugin("tool", "onesig_v2_5_3_D20250710")
        install_path = Path(record.installPath)
        assert install_path.parent.name == "api"
        assert (install_path / "_provider.yaml").is_file()

        # Catalog reflects the install — state flips to ``installed``.
        installed_entries = catalog.list_catalog(plugin_type="tool", state=["installed"])
        assert any(e.id == "onesig_v2_5_3_D20250710" for e in installed_entries)

        removed = await uninstall_plugin("tool", "onesig_v2_5_3_D20250710")
        assert removed is True
        assert not install_path.exists()

        # Back to ``available`` after uninstall — the bundled copy is still
        # on disk in flockshub.
        available_entries = catalog.list_catalog(plugin_type="tool", state=["available"])
        assert any(e.id == "onesig_v2_5_3_D20250710" for e in available_entries)

    async def test_two_versions_install_side_by_side(self, isolated_hub):
        """Different ``service_id`` + same conceptual product can coexist.

        We use distinct ``service_id`` values here because each install
        ID must be unique on disk; the api-versioning layer's
        same-``service_id``-different-version case is covered separately
        in ``tests/config/test_api_versioning.py``.
        """
        _write_bundled_tool(
            isolated_hub["bundled"],
            plugin_id="onesig_v2_5_3_D20250710",
            service_id="onesig_v2_5_3_D20250710_api",
            version="2.5.3 D20250710",
        )
        _write_bundled_tool(
            isolated_hub["bundled"],
            plugin_id="onesig_v2_5_3_D20260321",
            service_id="onesig_api",
            version="2.5.3 D20260321",
        )

        old = await install_plugin("tool", "onesig_v2_5_3_D20250710")
        new = await install_plugin("tool", "onesig_v2_5_3_D20260321")

        for record in (old, new):
            assert Path(record.installPath).parent.name == "api"
            assert Path(record.installPath).is_dir()

        from flocks.config.api_versioning import discover_api_service_descriptors

        descriptors = {d.storage_key for d in discover_api_service_descriptors(refresh=True)}
        assert "onesig_v2_5_3_D20250710_api_v2_5_3_D20250710" in descriptors
        assert "onesig_api_v2_5_3_D20260321" in descriptors


